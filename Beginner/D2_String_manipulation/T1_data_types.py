# Subscripting
print("Hello"[0])
print("Hello"[4])

# Tip, subscripting can be negative to count from right to left
print("Hello"[-4])

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)

# Quiz
street_name = "Abbey Road"
print(street_name[4] + street_name[7])